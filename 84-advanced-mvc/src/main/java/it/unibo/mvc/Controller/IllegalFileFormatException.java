package it.unibo.mvc.Controller;

public class IllegalFileFormatException extends java.lang.RuntimeException {
  
  public IllegalFileFormatException() {
    super();
  }
  
  public IllegalFileFormatException(java.lang.String arg0) {
    super(arg0);
  }
  
  public IllegalFileFormatException(java.lang.String arg0, java.lang.Throwable arg1) {
    super(arg0, arg1);
  }
  
  public IllegalFileFormatException(java.lang.Throwable arg0) {
    super(arg0);
  }
}